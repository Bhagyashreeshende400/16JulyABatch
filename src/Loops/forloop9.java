package Loops;

public class forloop9 
{
public static void main(String[] args) 
{
for(int i=5; i<=50; i=i+5)    //1st logic we using Addition
{
System.out.println(i);
}
for(int i=1; i<=10; i++)     // 2nd Logic we using Multiplication
{
	System.out.println(i*5);
}

}
}

	
	
	

